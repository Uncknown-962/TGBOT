def get_faq_list() -> str:
    """Возвращает список всех вопросов FAQ"""
    faq_list = "<b>📚 Часто задаваемые вопросы:</b>\n\n"

    for i, (key, data) in enumerate(FAQ_DATA.items(), 1):
        faq_list += f"{i}. {data['question']}\n"

    faq_list += "\nДля получения ответа используйте команду:\n/faq [номер вопроса]"

    return faq_list


def get_faq_answer(faq_id: int) -> str:
    """Возвращает ответ на вопрос FAQ"""
    keys = list(FAQ_DATA.keys())

    if 1 <= faq_id <= len(keys):
        key = keys[faq_id - 1]
        data = FAQ_DATA[key]
        return f"<b>❓ {data['question']}</b>\n\n{data['answer']}"
    else:
        return "❌ Вопрос с таким номером не найден. Используйте /faq для просмотра списка."


def search_faq(query: str) -> str:
    """Поиск по FAQ"""
    query = query.lower()
    results = []

    for i, (key, data) in enumerate(FAQ_DATA.items(), 1):
        if query in data['question'].lower() or query in data['answer'].lower():
            results.append(f"{i}. {data['question']}")

    if results:
        return "<b>🔍 Результаты поиска:</b>\n\n" + "\n".join(results)
    else:
        return "❌ По вашему запросу ничего не найдено. Попробуйте другие ключевые слова."
